package com.jacksonfdam.beam.i18n

/**
 * Supported UI languages. [tag] is the BCP-47 code; [label] is the short chip
 * shown in the in-app language selector. Swedish is `sv` (the language), not
 * `se` (which is the country code for Sweden).
 */
enum class Language(val tag: String, val label: String) {
    EN("en", "EN"),
    PT_BR("pt-BR", "PT"),
    ES("es", "ES"),
    SV("sv", "SV"),
}

/**
 * Every user-facing UI string, one bundle per language. Parameterised strings
 * keep `{tokens}` that the helper extensions below substitute. Brand words
 * ("Beam") and pure symbols are intentionally left untranslated.
 */
data class BeamStrings(
    // pairing
    val connectToHost: String,
    val pairingHint: String,
    val scanQrCode: String,
    val orEnterManually: String,
    val hostIp: String,
    val portOptional: String,
    val pin: String,
    val yourNameOptional: String,
    val connecting: String,
    val connect: String,
    // deck picker
    val chooseADeck: String,
    val noDecksAvailable: String,
    val slides: String,
    val notes: String,
    // header
    val connected: String,
    val disconnect: String,
    // navigation
    val prev: String,
    val next: String,
    val first: String,
    val last: String,
    // drawing + modes
    val hideDrawing: String,
    val drawOnSlide: String,
    val drawSpotlight: String,
    val slidesMode: String,
    val screenMode: String,
    val annotate: String,
    val interact: String,
    val pen: String,
    val marker: String,
    val spotlight: String,
    val projectingYourScreen: String,
    val screenModeBody: String,
    val slidePreviewCd: String,
    val slideLabel: String,
    val clearInk: String,
    val resetZoom: String,
    // notes + timer
    val speakerNotes: String,
    val noNotesForSlide: String,
    val noNotesSidecar: String,
    val loadDeckToSeeNotes: String,
    val timer: String,
    val start: String,
    val pause: String,
    val reset: String,
    val running: String,
    val paused: String,
    // desktop presenter
    val scanToConnect: String,
    val connectionQrCd: String,
    val sameWifi: String,
    val remoteConnectedOne: String,
    val remoteConnectedMany: String,
    val openAPdf: String,
    val openAnotherPdf: String,
    val noDeckLoaded: String,
    val slideXofY: String,
    val openPdfPressStart: String,
    val nextLabel: String,
    val end: String,
    val connectionTitle: String,
    val startingServer: String,
    val couldntStartServer: String,
    val portHeld: String,
    val currentSlideCd: String,
    val nextSlideCd: String,
    val waitingForDeck: String,
)

fun stringsFor(language: Language): BeamStrings = when (language) {
    Language.EN -> EnglishStrings
    Language.PT_BR -> PortugueseStrings
    Language.ES -> SpanishStrings
    Language.SV -> SwedishStrings
}

// --- token helpers ---------------------------------------------------------

fun BeamStrings.portOptionalText(port: Int): String = portOptional.replace("{port}", port.toString())
fun BeamStrings.connectionQrText(host: String): String = connectionQrCd.replace("{host}", host)
fun BeamStrings.portHeldText(port: Int): String = portHeld.replace("{port}", port.toString())
fun BeamStrings.slideXofYText(index: Int, total: Int): String =
    slideXofY.replace("{i}", index.toString()).replace("{total}", total.toString())
fun BeamStrings.remotesConnectedText(count: Int): String =
    (if (count == 1) remoteConnectedOne else remoteConnectedMany).replace("{n}", count.toString())
fun BeamStrings.deckSubtitleText(slideCount: Int, hasNotes: Boolean): String =
    "$slideCount $slides" + if (hasNotes) " · $notes" else ""

